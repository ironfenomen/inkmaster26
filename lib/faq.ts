function acceptedAnswerText(item: FaqItem): string {
  if (!item.deepDive) return item.a;
  const origin = getPublicSiteUrl();
  const linkPhrase = item.blogLinkText ?? "Подробнее в блоге";
  return `${item.a} ${linkPhrase}: ${origin}${item.deepDive}`;
}

